<p class="text-content">&nbsp;&nbsp;<em>YSCON(Collegiate Startup Conference)</em>은 전국의 창업동아리들이 주축이 되어 개최하는 대학생 창업 컨퍼런스로, 스타트업에 도전하고 있거나 관심 있는 전국의 학생들이 모여 교류하고 서로의 경험을 통해 배우는 자리입니다.</p>


<p class="text-content">&nbsp;&nbsp;그동안 한국에 학생 창업가들이 창업에 대한 이야기와 관심을 나누고 교류할 수 있는 행사가 미흡하다는 목소리가 많아 왔습니다. 이에 이런 아쉬움을 해결해 보고자 2016년 초 출발한 YSCON은 연 2회 개최되는 정기 컨퍼런스와 활발한 네트워킹 사업 참여를 통해 청년 창업가 네트워크 형성에 일조하고 있습니다.</p>

<p class="text-content">&nbsp;&nbsp;
YSCON은 동일한 눈높이에서 스타트업에 대한 이상과 꿈, 그리고 현실에 대해 자유롭게 대화하며. 스타트업을 통해 꿈에 다가가기까지 겪었던 수많은 어려움과 그 극복 과정에 관해 이야기를 나누며, 같은 학생으로서 공감할 수 있는 이야기의 장이 될 것입니다. 
</p>



<p class="text-content">&nbsp;&nbsp;
